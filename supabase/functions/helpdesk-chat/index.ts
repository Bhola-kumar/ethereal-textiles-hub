import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type",
};

serve(async (req) => {
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { messages, context } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    
    if (!LOVABLE_API_KEY) {
      throw new Error("LOVABLE_API_KEY is not configured");
    }

    const systemPrompt = `You are a helpful customer support assistant for an e-commerce platform called "GorakhDhanda". You help customers with:

1. **Product Inquiries**: Answer questions about products, specifications, pricing, availability, and recommendations.

2. **Order Issues**: Help with order status, tracking, delivery concerns, and order modifications.

3. **Returns & Refunds**: Guide customers through the return process, explain refund policies, and help initiate return requests.

4. **Cancellations**: Assist with order cancellations and explain the cancellation policy.

5. **General Support**: Answer general questions about shipping, payment methods, and platform policies.

**Guidelines:**
- Be friendly, professional, and empathetic
- Keep responses concise but helpful
- If a customer wants to cancel an order, return an item, or request a refund, guide them to use the appropriate action buttons that will appear in the chat
- For specific order issues, ask for the order number if not provided
- Always offer to help further at the end of your responses

**Context Information:**
${context ? JSON.stringify(context) : 'No additional context provided'}

Remember: You cannot directly process cancellations, returns, or refunds - you can only guide customers to use the action buttons or contact support for complex issues.`;

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-2.5-flash",
        messages: [
          { role: "system", content: systemPrompt },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "Rate limit exceeded. Please try again later." }), {
          status: 429,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "Service temporarily unavailable. Please try again later." }), {
          status: 402,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      const errorText = await response.text();
      console.error("AI gateway error:", response.status, errorText);
      return new Response(JSON.stringify({ error: "Failed to get AI response" }), {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (error) {
    console.error("Helpdesk chat error:", error);
    return new Response(JSON.stringify({ error: error instanceof Error ? error.message : "Unknown error" }), {
      status: 500,
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
